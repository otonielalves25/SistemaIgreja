    // CARREGA NOME NA TABELA //////////////////////////////////////////////////
    private void carregaTabela() {
        List<Membro> lista = membroDao.getListagem(txtPesquisa.getText());
        modeloTabela.setNumRows(0);
        for (Membro m : lista) {
            modeloTabela.addRow(new Object[]{
                m.getIdMembro(),
                m.getNome(),
                m.getStatus(),
            });
        }

    }

