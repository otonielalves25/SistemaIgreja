    private void tabelaMembroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMembroMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() >= 2 && tabelaMembro.getRowCount() >= 1) {
            int linha = tabelaMembro.getSelectedRow();
            this.setCodigo((int) tabelaMembro.getValueAt(linha, 0));
            this.setName((String) tabelaMembro.getValueAt(linha, 1));
            this.setStatus((String) tabelaMembro.getValueAt(linha, 2));
            this.dispose();
        }
    }//GEN-LAST:event_tabelaMembroMouseClicked

