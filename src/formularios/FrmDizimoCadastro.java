    // CARREA NOMES NA COMBOBOX //////////////////////////////////////////////////
    private void carregaComboboxNome() {
        ArrayList<Membro> lista = membroDao.getListagem("");
        cboNome.addItem("Selecione...");
        for (int i = 0; i < lista.size(); i++) {
            cboNome.addItem(lista.get(i).getNome());
        }

    }

    // CARREA ANO NA COMBOBOX //////////////////////////////////////////////////
    private void carregaAno() {
        int ano = 2021;
        do {
            cboAno.addItem(ano + "");
            ano++;
        } while (ano < 2030);

    }

    // CARREGA DATA  ///////////////////////////////////////////////////////////
    private void dataHoje() {

        Date data = new Date();
        txtData.setDate(data);

    }

    // CARREGA ANO ATUAL NA COMBOBOX ///////////////////////////////////////
    private void anoAtual() {
        int ano = new Date().getYear();
        cboAno.setSelectedItem(ano);

    }

    // CARREGA O MES ATUAL NA COMBOBOX /////////////////////////////////////////
    private void mostraMesCombo() {
        int mes2 = new Date().getMonth();
        cboMes.setSelectedIndex(mes2);
    }

    //LIMPA OS CAMPOS //////////////////////////////////////////////////////////
    private void limpar() {
        txtRecibo.setText("");
        cboNome.setSelectedIndex(0);
        cboFormaPagamento.setSelectedIndex(0);
        txtValor.setText("R$ ");
        anoAtual();
        dataHoje();
        mostraMesCombo();
        ckAtivo.setSelected(false);
        modeloTabela.setNumRows(0);
    }


