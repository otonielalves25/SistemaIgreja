    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String valorNovo = txtValor.getText().replace("R$ ", "").replace(",", "-").replace(".", "").replace("-", ".").replace("R$", "");

        if (cboNome.getSelectedItem().equals("Selecione...")) {
            JOptionPane.showMessageDialog(this, "Informe o nome do Membro.", "ERRO", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtData.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Informe uma data válida.", "ERRO", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (valorNovo.equalsIgnoreCase("0")) {
            JOptionPane.showMessageDialog(this, "Valor não foi informado.", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtValor.requestFocus();
            return;
        }
        membro_id = membroDao.getIdMembro(cboNome.getSelectedItem().toString());

        if (dizimoDao.verificaSeJaPagou(membro_id, cboMes.getSelectedItem().toString()) > 0 && novo) {
            JOptionPane.showMessageDialog(this, "Consta dizimo cadastrado esta Mês, verifique.", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtValor.requestFocus();
            return;
        }
    

        //membro_id = membroDao.getIdMembro(cboNome.getSelectedItem().toString());
        Dizimo dizimo = new Dizimo();
        dizimo.setDataCadastro(txtData.getDate());
        Membro m = new Membro(membro_id);
        dizimo.setMembro(m);
        dizimo.setMes(cboMes.getSelectedItem().toString());
        dizimo.setAno(Integer.parseInt(cboAno.getSelectedItem().toString()));
        dizimo.setFormaPaga(cboFormaPagamento.getSelectedItem().toString());
        dizimo.setValor(Float.parseFloat(valorNovo));
        if (novo) {
            if (dizimoDao.inserirNovo(dizimo)) {
                JOptionPane.showMessageDialog(this, "Dízimo cadastrado com sucesso !", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                limpar();
            } else {

                JOptionPane.showMessageDialog(this, "Erro ao cadastrar dízimo.", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            dizimo.setIdDizimo(Integer.parseInt(txtRecibo.getText()));
            if (dizimoDao.alterar(dizimo)) {
                JOptionPane.showMessageDialog(this, "Dízimo alterado com sucesso !", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                limpar();
            } else {

                JOptionPane.showMessageDialog(this, "Erro ao alterar dízimo.", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

